package com.hades.example.java._16_object._reusing._2_inheritance._arguments._3;

public class A {
    int num;

    public A() {
        System.out.println("A() constructor----->");
        System.out.println("A() constructor<-----");
    }

    public A(int num) {
        System.out.println("A(int) constructor----->");
        System.out.println("A(int) constructor<-----");
    }
}